def build_analysis_tasks(script_text: str) -> list[dict]:
    """Return an ordered list of (dimension_label, system_prompt, user_prompt)."""
    dimensions = [
        ("人物分析", CHARACTER_ANALYSIS),
        ("结构分析", STRUCTURE_ANALYSIS),
        ("对白分析", DIALOGUE_ANALYSIS),
        ("主题分析", THEME_ANALYSIS),
        ("综合评估", OVERALL_ASSESSMENT),
        ("图片提示词", IMAGE_PROMPTS),
        ("视频提示词", VIDEO_PROMPTS),
    ]
    tasks = []
    for label, prompt_template in dimensions:
        user_prompt = f"以下是一部短剧剧本的内容，请按指示进行分析：\n\n---\n{script_text}\n---"
        tasks.append({
            "label": label,
            "system": SYSTEM_ROLE,
            "user": user_prompt,
            "instruction": prompt_template,
        })
    return tasks
